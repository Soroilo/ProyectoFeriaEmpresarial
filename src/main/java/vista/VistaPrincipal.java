    private void btnRegistrarEmpresaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarEmpresaActionPerformed
    String nombre = txtNombre.getText();
    String sector = txtSector.getText();
    String email = txtEmail.getText();

    Empresa empresa = new Empresa(nombre, sector, email);
    empresaService.agregarEmpresa(empresa);

    // Limpiar campos despu√©s de registrar
    txtNombre.setText("");
    txtSector.setText("");
    txtEmail.setText("");

    // Actualizar la tabla de empresas
    actualizarTablaEmpresas();
}

